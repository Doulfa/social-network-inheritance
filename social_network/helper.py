def get_week_day_name(val_int):
    if val_int == 0:
        return 'Monday'
    if val_int == 1:
        return 'Tuesday'
    if val_int == 2:
        return 'Wednesday'
    if val_int == 3:
        return 'Thursday'
    if val_int == 4:
        return 'Friday'
    if val_int == 5:
        return 'Saturday'
    if val_int == 6:
        return 'Sunday'
    
